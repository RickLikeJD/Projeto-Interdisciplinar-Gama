    <p>
      Com o fim das atividades do Ministério, a Secretaria de Meio Ambiente do Maranhão obteve um Termo de Comodato em Brasília para a administração da área
    </p>
    <p>
        Após a realização de estudos, o Decreto nº 15.618 de 23 de junho de 1997 criou a Área de Proteção Ambiental do Itapiracó. A Reserva do Itapiracó é regulamentada como uma unidade de conservação de uso sustentável, classificação que autoriza o acesso do homem aos recursos naturais e até mesmo o povoamento da região
    </p>
    <p>
        Em 2017, o espaço ganhou mais de 15 praças, campos de futebol, futebol de praia e futevôlei, parquinhos infantis, circuito de skate, trilhas ecológicas, pista de cooper e áreas de estacionamento. O parque contém mais de 10 km de áreas para caminhada e foi considerado o maior centro de lazer do estado, também recebendo eventos e programações especiais.
